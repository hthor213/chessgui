_FIXTURE_LINES = [
    "e4 c5 Nf3 d6 d4 cxd4 Nxd4 Nf6 Nc3 a6 Be2 e5 Nb3 Be7 O-O O-O".split(),
    "d4 Nf6 c4 g6 Nc3 Bg7 e4 d6 Nf3 O-O Be2 e5 O-O Nc6 d5 Ne7".split(),
    "d4 d5 c4 e6 Nc3 Nf6 Bg5 Be7 e3 O-O Nf3 h6 Bh4 b6 Be2 Bb7".split(),
    "c4 e5 Nc3 Nf6 Nf3 Nc6 g3 d5 cxd5 Nxd5 Bg2 Nb6 O-O Be7 d3 O-O".split(),
    "e4 e5 Nf3 Nc6 Bb5 a6 Ba4 Nf6 O-O Be7 Re1 b5 Bb3 d6 c3 O-O".split(),
]
_RESULTS = ["1-0", "0-1", "1/2-1/2"]


def _fixture_pgn(n_games: int, player: str, event: str = "Fixture Open",
                 start_index: int = 0) -> str:
    """n distinct, legal games with `player` alternating colors."""
    out = []
    for i in range(n_games):
        k = start_index + i
        game = chess.pgn.Game()
        white, black = (player, f"Opponent {k}") if k % 2 == 0 else (f"Opponent {k}", player)
        game.headers["Event"] = event
        game.headers["Site"] = "Selftest"
        game.headers["Date"] = f"2024.01.{(k % 28) + 1:02d}"
        game.headers["Round"] = str(k + 1)
        game.headers["White"] = white
        game.headers["Black"] = black
        game.headers["Result"] = _RESULTS[k % 3]
        game.headers["WhiteElo"] = "1620"
        game.headers["BlackElo"] = "1580"
        node = game
        for san in _FIXTURE_LINES[k % len(_FIXTURE_LINES)]:
            node = node.add_variation(node.board().parse_san(san))
        out.append(str(game))
    return "\n\n".join(out) + "\n"


def _run_fixture(td: Path, name: str, n: int, tag: str, **kw) -> tuple[dict, Path]:
    fixture = td / f"{tag}.fixture.pgn"
    fixture.write_text(_fixture_pgn(n, name), encoding="utf-8")
    out_dir = td / f"{tag}.out"
    opts = Options(name=name, slug=slugify(f"{name} {tag}"), pgns=[fixture],
                   out_dir=out_dir, **kw)
    return run_pipeline(opts), out_dir


def self_test() -> int:
    ok = True

    def check(cond: bool, msg: str):
        nonlocal ok
        print(f"  [{'PASS' if cond else 'FAIL'}] {msg}")
        if not cond:
            ok = False

    with tempfile.TemporaryDirectory(prefix="bpp-selftest-") as td_s:
        td = Path(td_s)
        name = "Testy Fixture"

        # Gate 1: >= 30 games -> full persona.
        profile, out = _run_fixture(td, name, 35, "full")
        slug = profile["slug"]
        check(profile["sample"]["verdict"] == VERDICT_FULL, "35 games -> verdict full")
        check((out / f"{slug}.config.json").exists(), "full: config.json emitted")
        cfg = json.loads((out / f"{slug}.config.json").read_text())
        check(cfg["low_confidence"] is False, "full: config not low-confidence")
        check(cfg["sample"]["verdict"] == VERDICT_FULL, "full: verdict echoed in config")
        for ext in ("pgn", "SOURCES.md", "stats.json", "book.json", "profile.json"):
            check((out / f"{slug}.{ext}").exists(), f"full: {ext} emitted")

        # Provenance: every corpus game has Source + SourceDate.
        games = read_games(out / f"{slug}.pgn")
        check(len(games) == 35, "full: corpus keeps all 35 games")
        check(all(g.headers.get("Source") and g.headers.get("SourceDate") for g in games),
              "full: every game carries Source + SourceDate")

        # Stats dossier shape.
        stats = json.loads((out / f"{slug}.stats.json").read_text())
        check(stats["games"]["total"] == 35
              and stats["games"]["as_white"] + stats["games"]["as_black"] == 35,
              "full: stats colors add up")
        check(stats["results"]["wins"] + stats["results"]["draws"]
              + stats["results"]["losses"] == 35, "full: stats results add up")
        check(bool(stats["opening_families"]["as_white"]), "full: opening families present")
        phases = stats["phase_profile"]["ended_in"]
        check(sum(p["games"] for p in phases.values()) == 35, "full: phase profile adds up")

        # Gate 2: 10..29 -> LOW-CONFIDENCE config.
        profile, out = _run_fixture(td, name, 12, "low")
        slug = profile["slug"]
        check(profile["sample"]["verdict"] == VERDICT_LOW, "12 games -> verdict low-confidence")
        check(profile["sample"]["badge"] == "LOW-CONFIDENCE", "low: badge recorded")
        cfg = json.loads((out / f"{slug}.config.json").read_text())
        check(cfg["low_confidence"] is True, "low: config flagged low_confidence")
        check("LOW-CONFIDENCE" in cfg["strength_label"]["note"], "low: label says so")

        # Gate 3: < 10 -> dossier only, no config, reason recorded.
        profile, out = _run_fixture(td, name, 5, "tiny")
        slug = profile["slug"]
        check(profile["sample"]["verdict"] == VERDICT_DOSSIER, "5 games -> dossier-only")
        check(not (out / f"{slug}.config.json").exists(), "tiny: no config emitted")
        check(profile["artifacts"]["config"] is None, "tiny: profile says config absent")
        check(any("persona minimum" in r for r in profile["sample"]["reasons"]),
              "tiny: verdict says why")
        check((out / f"{slug}.book.json").exists() and (out / f"{slug}.stats.json").exists(),
              "tiny: dossier (stats + book) still emitted")

        # Unverified-event rule: attributed games don't count toward the floor.
        fixture = td / "mixed.fixture.pgn"
        fixture.write_text(_fixture_pgn(28, name) + "\n"
                           + _fixture_pgn(7, name, event="Sweden Open", start_index=28),
                           encoding="utf-8")
        out = td / "mixed.out"
        profile = run_pipeline(Options(name=name, slug="testy-mixed", pgns=[fixture],
                                       out_dir=out, unverified_event="Sweden"))
        s = profile["sample"]
        check(s["games"] == 35 and s["unverified_games"] == 7 and s["verified_games"] == 28,
              "unverified rule: 35 total / 28 verified")
        check(s["verdict"] == VERDICT_LOW, "unverified rule: 28 verified -> low-confidence")

        # --dossier-only blocks the persona regardless of count, and removes a
        # stale config left from an earlier run.
        check((out / "testy-mixed.config.json").exists(), "stale-config precondition")
        profile = run_pipeline(Options(name=name, slug="testy-mixed", pgns=[fixture],
                                       out_dir=out, unverified_event="Sweden",
                                       dossier_only="STAGING: review pending"))
        check(profile["sample"]["verdict"] == VERDICT_DOSSIER
              and "STAGING: review pending" in profile["sample"]["reasons"],
              "--dossier-only: verdict + reason recorded")
        check(not (out / "testy-mixed.config.json").exists(),
              "--dossier-only: stale config removed")

        # Dedupe: the same fixture passed twice yields one copy of each game.
        out = td / "dupes.out"
        f1 = td / "full.fixture.pgn"
        profile = run_pipeline(Options(name=name, slug="testy-dupes", pgns=[f1, f1],
                                       out_dir=out))
        check(profile["sample"]["games"] == 35, "dedupe: duplicate source collapses")

        # In-place: running on an existing staged corpus (hand-written
        # SOURCES.md, no marker) rewrites neither the PGN nor the provenance.
        out = td / "inplace.out"
        out.mkdir()
        staged_pgn = out / "testy-staged.pgn"
        staged_pgn.write_text(_fixture_pgn(11, name), encoding="utf-8")
        staged_sources = out / "testy-staged.SOURCES.md"
        staged_sources.write_text("# hand-written provenance\n", encoding="utf-8")
        pgn_before, md_before = staged_pgn.read_text(), staged_sources.read_text()
        profile = run_pipeline(Options(name=name, slug="testy-staged",
                                       pgns=[staged_pgn], out_dir=out))
        check(staged_pgn.read_text() == pgn_before, "in-place: corpus PGN untouched")
        check(staged_sources.read_text() == md_before, "in-place: hand-written SOURCES.md untouched")
        check(profile["notes"] == {"corpus_rewritten": False, "sources_md_written": False},
              "in-place: profile records the preservation")

        # Book sanity: entries respect max_ply and carry weights.
        book = json.loads((td / "full.out" / f"{slugify(name + ' full')}.book.json").read_text())
        check(book["stats"]["positions"] > 0
              and all(e["ply"] <= book["max_ply"] for e in book["entries"]),
              "book: positions within max_ply")

    return 0 if ok else 1


