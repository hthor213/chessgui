RIVALS_DIR = REPO_ROOT / "data" / "rivals"
SCRIPTS_DIR = REPO_ROOT / "scripts"

# --- Sample-honesty gates (spec 225) — the constants, not vibes in UI copy ---
FULL_PERSONA_FLOOR = 30  # >= this many verified games: full persona
PERSONA_MIN_GAMES = 10   # below this: dossier only, no persona config

VERDICT_FULL = "full"
VERDICT_LOW = "low-confidence"
VERDICT_DOSSIER = "dossier-only"
BADGES = {VERDICT_FULL: None, VERDICT_LOW: "LOW-CONFIDENCE", VERDICT_DOSSIER: "DOSSIER-ONLY"}

# Phase boundaries by ply — same convention as acpl_report.py (book/middle/end).
OPENING_MAX_PLY = 24
MIDDLE_MAX_PLY = 80

# Maia policy bands — same set as build_rival_configs.py / src-tauri maia.rs.
MAIA_BANDS = [1100, 1200, 1300, 1400, 1500, 1600, 1700, 1800, 1900]
DEFAULT_RATING = 1500  # used (and honestly labeled) only when no rating is found

SOURCES_MARKER = "<!-- generated by build_player_profile.py -->"


