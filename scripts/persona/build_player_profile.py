def main() -> int:
    ap = argparse.ArgumentParser(
        description="Build the full data/rivals profile artifact set for any player (spec 225).")
    ap.add_argument("name", nargs="?", help='display name, e.g. "Arnthor Einarsson"')
    ap.add_argument("--slug", default=None, help="artifact slug (default: slugified name)")
    ap.add_argument("--fide-id", default=None, help="FIDE ID (identity metadata only)")
    ap.add_argument("--chesscom", default=None, help="chess.com username to fetch")
    ap.add_argument("--lichess", default=None, help="lichess username to fetch")
    ap.add_argument("--pgn", action="append", type=Path, default=[],
                    help="PGN file to import (repeatable — the OTB path)")
    ap.add_argument("--alias", action="append", default=[],
                    help="extra name form to match in PGN headers (repeatable)")
    ap.add_argument("--out-dir", type=Path, default=RIVALS_DIR)
    ap.add_argument("--rating", type=int, default=None,
                    help="rating override for Maia band choice (else corpus Elo mean)")
    ap.add_argument("--rating-source", default=None, help="where --rating came from")
    ap.add_argument("--relationship", default="player")
    ap.add_argument("--unverified-event", default=None, metavar="REGEX",
                    help="games whose Event matches stay in the dossier but do not "
                         "count toward the persona floor (attribution unconfirmed)")
    ap.add_argument("--dossier-only", default=None, metavar="REASON",
                    help="emit no persona config regardless of sample; REASON is "
                         "recorded in the verdict")
    ap.add_argument("--max-ply", type=int, default=book_builder.MAX_PLY,
                    help=f"book depth in plies (default {book_builder.MAX_PLY})")
    ap.add_argument("--since", default=None, help="fetchers: earliest month, YYYY-MM")
    ap.add_argument("--time-class", default=None, help="fetchers: comma list to keep")
    ap.add_argument("--self-test", action="store_true", help="run built-in checks and exit")
    args = ap.parse_args()

    if args.self_test:
        print("build_player_profile self-test:")
        return self_test()
    if not args.name:
        ap.error("name is required (or use --self-test)")

    missing = [str(p) for p in args.pgn if not p.exists()]
    if missing:
        print(f"error: PGN(s) not found: {', '.join(missing)}", file=sys.stderr)
        return 1

    opts = Options(name=args.name, slug=args.slug or slugify(args.name),
                   fide_id=args.fide_id, chesscom=args.chesscom, lichess=args.lichess,
                   pgns=args.pgn, aliases=args.alias, out_dir=args.out_dir,
                   rating=args.rating, rating_source=args.rating_source,
                   relationship=args.relationship,
                   unverified_event=args.unverified_event,
                   dossier_only=args.dossier_only, max_ply=args.max_ply,
                   since=args.since, time_class=args.time_class)
    run_pipeline(opts)
    return 0


if __name__ == "__main__":
    sys.exit(main())
