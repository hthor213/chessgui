@dataclass
class Options:
    name: str
    slug: str
    fide_id: str | None = None
    chesscom: str | None = None
    lichess: str | None = None
    pgns: list[Path] = field(default_factory=list)
    aliases: list[str] = field(default_factory=list)
    out_dir: Path = RIVALS_DIR
    rating: int | None = None
    rating_source: str | None = None
    relationship: str = "player"
    unverified_event: str | None = None
    dossier_only: str | None = None
    max_ply: int = book_builder.MAX_PLY
    since: str | None = None
    time_class: str | None = None


def slugify(name: str) -> str:
    s = name.strip().lower()
    # Icelandic/latin-1 folds seen in existing slugs (arnthor-einarsson precedent).
    for a, b in (("þ", "th"), ("ð", "d"), ("æ", "ae"), ("ö", "o"), ("á", "a"),
                 ("é", "e"), ("í", "i"), ("ó", "o"), ("ú", "u"), ("ý", "y")):
        s = s.replace(a, b)
    s = re.sub(r"[^a-z0-9]+", "-", s).strip("-")
    return s or "player"


# ---------------------------------------------------------------------------
# Player matching — headers carry usernames online and "First Last" /
# "Last, First" over the board; match any known alias in either form.
# ---------------------------------------------------------------------------

def _norm_name(s: str) -> str:
    s = " ".join(s.strip().lower().split())
    if "," in s:  # "Einarsson, Arnthor" -> "arnthor einarsson"
        last, _, first = s.partition(",")
        s = f"{first.strip()} {last.strip()}"
    return s


def alias_set(opts: Options) -> set[str]:
    names = [opts.name] + opts.aliases
    for u in (opts.chesscom, opts.lichess):
        if u:
            names.append(u)
    return {_norm_name(n) for n in names if n and n.strip()}


def player_color(game: chess.pgn.Game, aliases: set[str]) -> str | None:
    if _norm_name(game.headers.get("White", "")) in aliases:
        return "white"
    if _norm_name(game.headers.get("Black", "")) in aliases:
        return "black"
    return None


# ---------------------------------------------------------------------------
# Corpus assembly: fetch/import -> provenance headers -> dedupe
# ---------------------------------------------------------------------------

@dataclass
class SourceRecord:
    kind: str      # "chess.com" | "lichess" | "pgn"
    detail: str    # username or file path
    path: Path     # PGN on disk
    games: int = 0     # games in the file
    matched: int = 0   # games where the player was identified


def run_fetcher(script: str, user: str, out: Path, opts: Options) -> None:
    cmd = [sys.executable, str(SCRIPTS_DIR / script), user, "-o", str(out)]
    if opts.since:
        cmd += ["--since", opts.since]
    if opts.time_class:
        cmd += ["--time-class", opts.time_class]
    subprocess.run(cmd, check=True)


def gather_sources(opts: Options, tmpdir: Path) -> list[SourceRecord]:
    sources: list[SourceRecord] = []
    if opts.chesscom:
        p = tmpdir / f"{opts.slug}.chesscom.pgn"
        run_fetcher("fetch_chesscom.py", opts.chesscom, p, opts)
        sources.append(SourceRecord("chess.com", opts.chesscom, p))
    if opts.lichess:
        p = tmpdir / f"{opts.slug}.lichess.pgn"
        run_fetcher("fetch_lichess.py", opts.lichess, p, opts)
        sources.append(SourceRecord("lichess", opts.lichess, p))
    for pgn in opts.pgns:
        sources.append(SourceRecord("pgn", str(pgn), Path(pgn)))
    return sources


def read_games(path: Path) -> list[chess.pgn.Game]:
    games = []
    with open(path, encoding="utf-8", errors="replace") as fh:
        while True:
            g = chess.pgn.read_game(fh)
            if g is None:
                break
            games.append(g)
    return games


def ensure_provenance(game: chess.pgn.Game, fallback: str, stamp: str) -> None:
    """Every corpus game keeps Source + SourceDate (arnthor SOURCES.md precedent).
    chess.com PGNs carry [Link], lichess carries a URL [Site]; either is the source."""
    if not game.headers.get("Source"):
        link = game.headers.get("Link", "")
        site = game.headers.get("Site", "")
        url = link if link.startswith("http") else (site if site.startswith("http") else "")
        game.headers["Source"] = url or fallback
    if not game.headers.get("SourceDate"):
        game.headers["SourceDate"] = stamp


def dedupe_key(game: chess.pgn.Game) -> str:
    src = game.headers.get("Source", "")
    if src.startswith("http"):
        return src
    raw = "|".join((game.headers.get("White", ""), game.headers.get("Black", ""),
                    game.headers.get("Date", ""), game.headers.get("Result", ""),
                    " ".join(m.uci() for m in game.mainline_moves())))
    return hashlib.sha256(raw.encode()).hexdigest()


@dataclass
class Corpus:
    games: list[tuple[chess.pgn.Game, str]] = field(default_factory=list)  # (game, color)
    dropped_unmatched: int = 0
    duplicates: int = 0
    sources: list[SourceRecord] = field(default_factory=list)


def assemble_corpus(opts: Options, sources: list[SourceRecord]) -> Corpus:
    aliases = alias_set(opts)
    stamp = date.today().isoformat()
    corpus = Corpus(sources=sources)
    seen: set[str] = set()
    for src in sources:
        fallback = f"{src.kind}:{Path(src.detail).name if src.kind == 'pgn' else src.detail}"
        for game in read_games(src.path):
            src.games += 1
            color = player_color(game, aliases)
            if color is None:
                corpus.dropped_unmatched += 1
                continue
            src.matched += 1
            ensure_provenance(game, fallback, stamp)
            key = dedupe_key(game)
            if key in seen:
                corpus.duplicates += 1
                continue
            seen.add(key)
            corpus.games.append((game, color))
    return corpus


# ---------------------------------------------------------------------------
# Stats dossier: colors, results, opening families, phase win/loss profile
# ---------------------------------------------------------------------------

def opening_family(sans: list[str], headers: chess.pgn.Headers) -> str:
    """Coarse family label. Prefers the PGN's own Opening header (lichess /
    chess.com online games carry one); falls back to a first-moves table
    (OTB corpora like arnthor's carry no ECO/Opening headers)."""
    opening = headers.get("Opening", "").strip()
    if opening and opening != "?":
        return opening.split(":")[0].split(",")[0].strip()
    if not sans:
        return "unknown"
    first, reply = sans[0], (sans[1] if len(sans) > 1 else "")
    if first == "e4":
        return {"c5": "Sicilian", "e5": "Open Game (1.e4 e5)", "e6": "French",
                "c6": "Caro-Kann", "d6": "Pirc/Modern", "g6": "Pirc/Modern",
                "d5": "Scandinavian", "Nf6": "Alekhine"}.get(reply, "Other 1.e4")
    if first == "d4":
        if reply == "d5":
            return "Queen's Gambit / Closed (1.d4 d5)"
        if reply == "Nf6":
            early = sans[2:6]
            if "g6" in early:
                return "Indian: KID/Grünfeld complex"
            if "e6" in early:
                return "Indian: Nimzo/QID complex"
            return "Indian (1.d4 Nf6)"
        return "Other 1.d4"
    return {"c4": "English", "Nf3": "Réti/Nf3 systems", "f4": "Bird",
            "b3": "Nimzo-Larsen", "g3": "King's fianchetto"}.get(first, "Irregular")


def _line_str(sans: list[str], plies: int) -> str:
    return " ".join(book_builder._san_with_number(i, s) for i, s in enumerate(sans[:plies]))


def ending_phase(total_plies: int) -> str:
    if total_plies <= OPENING_MAX_PLY:
        return "opening"
    if total_plies <= MIDDLE_MAX_PLY:
        return "middlegame"
    return "endgame"


def _wdl() -> dict:
    return {"wins": 0, "draws": 0, "losses": 0, "games": 0}


def _tally(bucket: dict, outcome: str | None) -> None:
    bucket["games"] += 1
    if outcome:
        bucket[outcome] += 1


def build_stats(opts: Options, corpus: Corpus) -> dict:
    overall, by_color = _wdl(), {"white": _wdl(), "black": _wdl()}
    phases = {"opening": _wdl(), "middlegame": _wdl(), "endgame": _wdl()}
    families: dict[str, dict[str, dict]] = {"white": {}, "black": {}}
    lines: dict[str, dict[str, int]] = {"white": {}, "black": {}}
    elos: list[int] = []
    dates: list[str] = []

    for game, color in corpus.games:
        sans, board = [], game.board()
        for move in game.mainline_moves():
            try:
                sans.append(board.san(move))
            except (ValueError, AssertionError):
                break
            board.push(move)

        result = game.headers.get("Result", "*")
        outcome = None  # "wins" | "draws" | "losses" from the player's side
        if result == "1/2-1/2":
            outcome = "draws"
        elif result in ("1-0", "0-1"):
            won_white = result == "1-0"
            outcome = "wins" if (won_white == (color == "white")) else "losses"

        _tally(overall, outcome)
        _tally(by_color[color], outcome)
        _tally(phases[ending_phase(len(sans))], outcome)

        fam = families[color].setdefault(opening_family(sans, game.headers), _wdl())
        _tally(fam, outcome)
        line = _line_str(sans, 6)
        if line:
            lines[color][line] = lines[color].get(line, 0) + 1

        elo = game.headers.get("WhiteElo" if color == "white" else "BlackElo", "")
        if elo.isdigit():
            elos.append(int(elo))
        d = game.headers.get("Date", "")
        if d and not d.startswith("?"):
            dates.append(d)

    def top_families(color: str) -> list[dict]:
        return [{"family": f, **w}
                for f, w in sorted(families[color].items(),
                                   key=lambda kv: -kv[1]["games"])]

    def top_lines(color: str) -> list[dict]:
        return [{"line": l, "games": n}
                for l, n in sorted(lines[color].items(), key=lambda kv: (-kv[1], kv[0]))[:10]]

    total = overall["games"]
    decided = overall["wins"] + overall["draws"] + overall["losses"]
    score = (overall["wins"] + overall["draws"] / 2) / decided if decided else None
    rating = rating_verdict(opts, elos)
    return {
        "version": 1,
        "generated_at": int(time.time()),
        "generator": "scripts/persona/build_player_profile.py",
        "slug": opts.slug,
        "display_name": opts.name,
        "games": {"total": total,
                  "as_white": by_color["white"]["games"],
                  "as_black": by_color["black"]["games"]},
        "results": {**{k: overall[k] for k in ("wins", "draws", "losses")},
                    "score_pct": round(score * 100, 1) if score is not None else None,
                    "as_white": by_color["white"], "as_black": by_color["black"]},
        "opening_families": {"as_white": top_families("white"),
                             "as_black": top_families("black")},
        "top_lines": {"as_white": top_lines("white"), "as_black": top_lines("black")},
        "phase_profile": {
            "boundaries": {"opening_max_ply": OPENING_MAX_PLY,
                           "middlegame_max_ply": MIDDLE_MAX_PLY},
            "ended_in": phases,
        },
        "rating": rating,
        "date_range": {"first": min(dates), "last": max(dates)} if dates else None,
    }


def rating_verdict(opts: Options, corpus_elos: list[int]) -> dict:
    if opts.rating is not None:
        return {"value": opts.rating,
                "source": opts.rating_source or "provided via --rating"}
    if corpus_elos:
        mean = round(sum(corpus_elos) / len(corpus_elos))
        return {"value": mean,
                "source": f"corpus Elo headers (mean of {len(corpus_elos)} games)"}
    return {"value": None, "source": "no rating found"}


# ---------------------------------------------------------------------------
# Sample verdict — the honesty record (spec 225)
# ---------------------------------------------------------------------------

def sample_verdict(opts: Options, corpus: Corpus) -> dict:
    total = len(corpus.games)
    unverified = 0
    if opts.unverified_event:
        rx = re.compile(opts.unverified_event, re.IGNORECASE)
        unverified = sum(1 for g, _ in corpus.games if rx.search(g.headers.get("Event", "")))
    verified = total - unverified

    reasons: list[str] = []
    if opts.dossier_only:
        verdict = VERDICT_DOSSIER
        reasons.append(opts.dossier_only)
    elif verified < PERSONA_MIN_GAMES:
        verdict = VERDICT_DOSSIER
        reasons.append(f"{verified} verified games < persona minimum {PERSONA_MIN_GAMES}")
    elif verified < FULL_PERSONA_FLOOR:
        verdict = VERDICT_LOW
        reasons.append(f"{verified} verified games < full-persona floor {FULL_PERSONA_FLOOR}")
    else:
        verdict = VERDICT_FULL
    if unverified:
        reasons.append(f"{unverified} games match unverified-event rule "
                       f"/{opts.unverified_event}/ and do not count toward the floor")

    return {
        "games": total,
        "verified_games": verified,
        "unverified_games": unverified,
        "unverified_rule": opts.unverified_event,
        "thresholds": {"full_persona_floor": FULL_PERSONA_FLOOR,
                       "persona_min_games": PERSONA_MIN_GAMES},
        "verdict": verdict,
        "badge": BADGES[verdict],
        "reasons": reasons,
    }


# ---------------------------------------------------------------------------
# Book + persona config (reusing the spec 214 machinery/schemas)
# ---------------------------------------------------------------------------

def build_book(opts: Options, corpus: Corpus, corpus_path: Path) -> dict:
    entries: "OrderedDict[tuple[str, str], book_builder.Entry]" = OrderedDict()
    stats = book_builder.BuildStats()
    for game, color in corpus.games:
        stats.games += 1
        if not book_builder.is_standard_start(game):
            stats.skipped_non_standard += 1
            continue
        if book_builder.harvest_game(game, color, entries, opts.max_ply):
            stats.used += 1
    stats.sources.append({"username": opts.name, "path": str(corpus_path),
                          "games": stats.games})
    ordered = sorted(entries.values(), key=lambda e: (-e.weight, e.ply, e.line))
    return book_builder.to_document(ordered, stats, rival=opts.slug, max_ply=opts.max_ply)


def nearest_band(rating: int) -> int:
    return min(MAIA_BANDS, key=lambda b: abs(b - rating))


def build_config(opts: Options, book: dict, verdict: dict, rating: dict) -> dict:
    """Persona config, build_rival_configs.py schema + the sample-honesty block."""
    rv = rating["value"] if rating["value"] is not None else DEFAULT_RATING
    band = nearest_band(rv)
    band_source = (f"{rating['source']} rating {rating['value']} -> nearest Maia band"
                   if rating["value"] is not None
                   else f"default {DEFAULT_RATING} (no rating found) -> nearest Maia band")
    low = verdict["verdict"] == VERDICT_LOW
    note = ("NOT harness-measured (too few games / private individual). Realism "
            "comes from the opening book + Maia policy; validated only by the "
            "in-app 'felt like him' feedback capture.")
    if low:
        note = (f"LOW-CONFIDENCE sample ({verdict['verified_games']} verified games "
                f"< {FULL_PERSONA_FLOOR}). " + note)
    return {
        "version": 1,
        "slug": opts.slug,
        "display_name": opts.name,
        "kind": "private-rival",
        "relationship": opts.relationship,
        "book": {"path": f"data/rivals/{opts.slug}.book.json",
                 "max_ply": book["max_ply"],
                 "positions": book["stats"]["positions"],
                 "games": book["stats"]["games_used"]},
        "backend": {"kind": "maia", "level": band, "net": f"maia-{band}.pb.gz"},
        "runnable_in_engine_v1": True,
        "sampling": {"level": band, "temperature": 0.5, "alpha": 1.0,
                     "lambda": 0.75, "top_k": 4, "verify_depth": 12},
        "strength_label": {"kind": "book + Maia band, unmeasured",
                           "maia_band": band, "band_source": band_source,
                           "note": note},
        "low_confidence": low,
        "sample": verdict,
        "private": True,
    }


# ---------------------------------------------------------------------------
# Artifact writing
# ---------------------------------------------------------------------------

def assert_writable(out_dir: Path) -> None:
    """Refuse to write inside the repo unless the target is gitignored
    (spec 214 guard, same as build_rival_configs.py). Paths outside the repo
    (self-test tmp dirs) are exempt."""
    try:
        rel = out_dir.resolve().relative_to(REPO_ROOT)
    except ValueError:
        return
    r = subprocess.run(["git", "-C", str(REPO_ROOT), "check-ignore", "-q",
                        str(rel / "probe.config.json")])
    if r.returncode != 0:
        raise SystemExit(f"REFUSING: {rel} is not gitignored — private player "
                         "profiles must never be committable (spec 214).")


def write_corpus(corpus: Corpus, out_pgn: Path) -> bool:
    """Write the merged corpus. Skipped (returns False) when the corpus was read
    in-place from out_pgn itself with no other sources — never rewrite a staged,
    hand-provenanced file just to reformat it."""
    in_place = (len(corpus.sources) == 1 and corpus.sources[0].kind == "pgn"
                and Path(corpus.sources[0].detail).resolve() == out_pgn.resolve())
    if in_place:
        return False
    exporter_text = []
    for game, _ in corpus.games:
        exporter_text.append(str(game).rstrip())
    out_pgn.write_text("\n\n".join(exporter_text) + "\n", encoding="utf-8")
    return True


def write_sources_md(opts: Options, corpus: Corpus, path: Path, corpus_written: bool) -> bool:
    """Generated provenance file. A hand-written SOURCES.md (no marker line —
    the arnthor precedent) is never touched."""
    if path.exists() and SOURCES_MARKER not in path.read_text(encoding="utf-8"):
        return False
    ids = [f"FIDE {opts.fide_id}" if opts.fide_id else None,
           f"chess.com `{opts.chesscom}`" if opts.chesscom else None,
           f"lichess `{opts.lichess}`" if opts.lichess else None]
    ids_str = ", ".join(i for i in ids if i) or "no external IDs recorded"
    rows = "\n".join(f"| {s.kind} | {s.detail} | {s.games} | {s.matched} |"
                     for s in corpus.sources)
    body = f"""# Provenance — {opts.slug}.pgn
{SOURCES_MARKER}

**Player:** {opts.name} ({ids_str})
**Assembled:** {date.today().isoformat()} by scripts/persona/build_player_profile.py.
Directory is gitignored; never commit (spec 214 hard rule).

## Sources

| kind | detail | games in source | games matched to player |
|---|---|---|---|
{rows}

Corpus: {len(corpus.games)} games kept ({corpus.duplicates} duplicates removed,
{corpus.dropped_unmatched} games dropped where the player was not identified).
Every kept game carries `[Source]` and `[SourceDate]` headers.
{"" if corpus_written else "Corpus file was reused in place (single PGN source); not rewritten."}
"""
    path.write_text(body, encoding="utf-8")
    return True


def _write_json(path: Path, doc: dict) -> None:
    path.write_text(json.dumps(doc, indent=1, ensure_ascii=False) + "\n", encoding="utf-8")


# ---------------------------------------------------------------------------
# Pipeline
# ---------------------------------------------------------------------------

def run_pipeline(opts: Options) -> dict:
    """Runs the whole pipeline; returns the profile document (also written to
    <slug>.profile.json)."""
    if not (opts.chesscom or opts.lichess or opts.pgns):
        raise SystemExit("no game sources: pass --chesscom, --lichess and/or --pgn "
                         "(a FIDE ID alone carries no games — FIDE has no game archive)")
    opts.out_dir.mkdir(parents=True, exist_ok=True)
    assert_writable(opts.out_dir)

    with tempfile.TemporaryDirectory(prefix="player-profile-") as td:
        sources = gather_sources(opts, Path(td))
        corpus = assemble_corpus(opts, sources)

        out_pgn = opts.out_dir / f"{opts.slug}.pgn"
        corpus_written = write_corpus(corpus, out_pgn)
        sources_path = opts.out_dir / f"{opts.slug}.SOURCES.md"
        sources_written = write_sources_md(opts, corpus, sources_path, corpus_written)

    stats = build_stats(opts, corpus)
    _write_json(opts.out_dir / f"{opts.slug}.stats.json", stats)

    book = build_book(opts, corpus, out_pgn)
    _write_json(opts.out_dir / f"{opts.slug}.book.json", book)

    verdict = sample_verdict(opts, corpus)
    config_path = opts.out_dir / f"{opts.slug}.config.json"
    if verdict["verdict"] == VERDICT_DOSSIER:
        # No persona below the floor (or while staged): make sure a stale config
        # from an earlier, larger-looking sample can't outlive the verdict.
        config_path.unlink(missing_ok=True)
        config_emitted = False
    else:
        _write_json(config_path, build_config(opts, book, verdict, stats["rating"]))
        config_emitted = True

    profile = {
        "version": 1,
        "generated_at": int(time.time()),
        "generator": "scripts/persona/build_player_profile.py",
        "slug": opts.slug,
        "display_name": opts.name,
        "relationship": opts.relationship,
        "identity": {"fide_id": opts.fide_id, "chesscom": opts.chesscom,
                     "lichess": opts.lichess},
        "sample": verdict,
        "rating": stats["rating"],
        "artifacts": {
            "pgn": f"data/rivals/{opts.slug}.pgn",
            "sources_md": f"data/rivals/{opts.slug}.SOURCES.md",
            "stats": f"data/rivals/{opts.slug}.stats.json",
            "book": f"data/rivals/{opts.slug}.book.json",
            "config": f"data/rivals/{opts.slug}.config.json" if config_emitted else None,
        },
        "notes": {
            "corpus_rewritten": corpus_written,
            "sources_md_written": sources_written,
        },
    }
    _write_json(opts.out_dir / f"{opts.slug}.profile.json", profile)

    print(f"{opts.slug}: {verdict['games']} games "
          f"({verdict['verified_games']} verified) -> verdict {verdict['verdict'].upper()}")
    for r in verdict["reasons"]:
        print(f"  - {r}")
    print(f"  book: {book['stats']['positions']} positions from "
          f"{book['stats']['games_used']} games (max_ply {book['max_ply']})")
    print(f"  config: {'written' if config_emitted else 'NOT emitted (dossier-only)'}")
    return profile


