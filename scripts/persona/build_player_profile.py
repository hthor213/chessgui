#!/usr/bin/env python3
"""Spec 225 Part 1: any-player profile pipeline.

One command turns a named player into the full data/rivals artifact set:

    <slug>.pgn          merged corpus, every game with Source/SourceDate headers
    <slug>.SOURCES.md   provenance (never overwrites a hand-written one)
    <slug>.stats.json   stats dossier: colors, results, opening families,
                        phase win/loss profile
    <slug>.profile.json identity + sample verdict — the single honesty record
                        every consumer renders from
    <slug>.book.json    opening book (build_rival_book.py machinery, generalized)
    <slug>.config.json  persona config (build_rival_configs.py schema) — ONLY
                        when the sample-honesty gates allow it

Inputs: a display name plus any of FIDE ID (metadata only — FIDE publishes no
game archive), chess.com username (fetched via scripts/fetch_chesscom.py),
lichess username (scripts/fetch_lichess.py), and/or PGN files (the OTB import
path — the arnthor corpus precedent).

Sample-honesty gates (spec 225, named constants below, calibrated on dad's
47-game book):
    >= 30 usable games  -> full persona (book + dossier + config)
    10..29              -> persona allowed but LOW-CONFIDENCE badged in config
    < 10                -> dossier only, NO config, verdict says why
The verdict (count, thresholds, badge) is stored in <slug>.profile.json and
echoed into <slug>.config.json so every surface renders the same honesty.
`--unverified-event REGEX` marks games whose attribution is unconfirmed (the
arnthor Sweden-block case): they stay in the corpus/dossier but do not count
toward the persona floor. `--dossier-only REASON` blocks the persona outright
(e.g. corpus still in user-review staging) and records the reason.

data/rivals is gitignored (spec 214 hard rule — private individuals stay
LOCAL); this script refuses to write there if that ever stops being true.

Usage:
    build_player_profile.py "Arnthor Einarsson" --pgn data/rivals/arnthor-einarsson.pgn \
        --fide-id 2300540 --unverified-event Sweden --dossier-only "STAGING: user review pending"
    build_player_profile.py "Some Friend" --chesscom somefriend --lichess some_friend
    build_player_profile.py --self-test
"""

